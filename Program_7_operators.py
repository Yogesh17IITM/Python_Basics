# 1) Arithmetic operator (+,-,*,/,%,//,**)
a,b=10,3
print(a%b)  #gives remainder value
print(a//b) #gives rounded-off value
print(a**b) #gives power of value a^b

# 2) Assignment operator (=,+=,-=,*=,/=)
x=y=z=3
x*=y
print(x)

# 3) Comparison operator (==,>,<,<=,>=,!=)
print(x!=z)

# 4) Logical operator(and,or,not)
print(x==y and y==z)
